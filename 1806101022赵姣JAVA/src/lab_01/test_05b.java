package lab_01;

public class test_05b {
    //switch描述
    public static void main(String args[]) {

        int score = 4;
        switch (score) {
            case 1:
                System.out.println("不及格");
                break;
            case 2:
                System.out.println("及格");
                break;
            case 3:
                System.out.println("中等");
                break;
            case 4:
                System.out.println(" 良好");
                break;
            case 5:
                System.out.println("优秀");
                break;
        }

    }

}

