package week_01;

public class Test_2_10 {
    public static void main(String args[]) {

        for (int k = 1; k <= 9; k++) {

            for (int j = 1; j <= k; j++) {

                System.out.print(j + "*" + k + "=" + (j * k) + "\t");

            }

            System.out.println();

        }

    }

}
